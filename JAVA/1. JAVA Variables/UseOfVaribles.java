public class UseOfVaribles {
    public static void main(String[] args){

        //Integer
        int Num=10;
        System.out.println("This is Integer : "+ Num);

        //Float
        float Decimal=10.5f;
        System.out.println("This is Float : " + Decimal);

        //Character
        char Letter='A';
        System.out.println("This is Character : " + Letter);

        //String
        String Name="Aman";
        System.out.println("This is String : " + Name);


    }
}
